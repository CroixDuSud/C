#include <stdio.h>
#include <conio.h>

int main()
{
    int t[2][4];
    int i, j;

    /* -------------------------------------------------------------------------- */
    /* Adresses des postes du tableau                                             */
    /* -------------------------------------------------------------------------- */
    for (i = 0; i < 2; i++)
    {
        for (j = 0; j < 4; j++)
        {
            printf("&t[%d][%d]=%p  ", i, j, &t[i][j]);
        }
        printf("\n");
    }
    printf("\n\n");

    printf("i = %d  j = %d", i, j);
    printf("\n\n");

    /* -------------------------------------------------------------------------- */
    /* Affichage de quelques adresses                                             */
    /* -------------------------------------------------------------------------- */
    printf("t     =   %p\n",   t);
    printf("t + 1 =   %p\n",   t + 1);
    printf("t[1]  =   %p\n\n", t[1]);

    return 0;
}
