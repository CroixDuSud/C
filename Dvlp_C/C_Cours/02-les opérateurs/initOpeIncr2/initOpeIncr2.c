#include <stdio.h>

int main()
{
    int i, j;

    i = 5;
    j = 4;

    printf("%d  i= %d j=%d  \n", ++i + j--, i, j);
    printf("%d  i= %d j=%d  \n", i--, i, j);
    printf("%d  i= %d j=%d  \n", ++j, i, j);

    return 0;
}




