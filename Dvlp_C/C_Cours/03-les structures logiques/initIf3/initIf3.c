#include <stdio.h>

int main()
{
    int a, b, c;

    printf("Introduisez trois nombres : ");
    scanf("%d %d %d", &a, &b, &c);

    /* -------------------------------------------------------------------------- */
    /* Tests imbriqués illisibles (et faux)                                       */
    /* -------------------------------------------------------------------------- */

    if (a <= b) if (b <= c) printf("Ordonne...\n");
        else                    printf("Non ordonne...\n");

    /* -------------------------------------------------------------------------- */
    /* Tests imbriqués                                                            */
    /* -------------------------------------------------------------------------- */

    if (a <= b)
    {
        if(b <= c)
        {
            printf("Ordonne...\n");
        }
        else
        {
            printf("Non ordonne...\n");
        }
    }
    else
    {
        printf("Non ordonne...\n");
    }

    /* -------------------------------------------------------------------------- */
    /* Test multiple                                                              */
    /* -------------------------------------------------------------------------- */

    if ((a <= b) && (b <= c))
    {
        printf("Ordonne...\n");
    }
    else
    {
        printf("Non ordonne...\n");
    }

    return 0;
}
